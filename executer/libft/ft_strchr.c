#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	char	*s1;

	s1 = (char *)str;
	while (*s1 != '\0' || (char)c == 0)
	{
		if (*s1 == (char)c)
		{
			return (s1);
		}
		s1++;
	}
	return (0);
}
